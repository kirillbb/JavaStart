package basic.syntax;

public class FlowControl {
    public static void  main(String[] args){
    }

    public  static  void  ifElseDemo(boolean var){
        if (var){
            System.out.println(1);
        }
        else if (!var){
            System.out.println(0);
        }
        else {
            System.out.println();
        }
    }
}
